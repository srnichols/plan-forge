            local got_desc
            got_desc="$(grep -oP 'description:\s*"?\K[^"]+' "$prompt_file" 2>/dev/null | head -1)" || true
            [ -n "$got_desc" ] && desc="$got_desc"

            {
                echo "---"
                echo "name: planforge-$base"
                echo "description: $desc"
                echo "metadata:"
                echo "  author: plan-forge"
                echo "  source: .github/prompts/$(basename "$prompt_file")"
                echo "user-invocable: true"
                echo "argument-hint: \"Provide context or parameters for this prompt\""
                echo "---"
                echo ""
                cat "$prompt_file"
            } > "$skill_file"
            green "  CREATE .claude/skills/planforge-$base/SKILL.md"
        done
    fi

    # Convert reviewer agents → Claude skills
    local agents_dir="$target/.github/agents"
    if [ -d "$agents_dir" ]; then
        for agent_file in "$agents_dir"/*.agent.md; do
            [ -f "$agent_file" ] || continue
            local base
            base="$(basename "$agent_file" .agent.md)"
            local skill_dir="$skills_dir/planforge-$base"
            local skill_file="$skill_dir/SKILL.md"
            mkdir -p "$skill_dir"

            local desc="Plan Forge reviewer agent"
            local got_desc
            got_desc="$(grep -oP 'description:\s*"?\K[^"]+' "$agent_file" 2>/dev/null | head -1)" || true
            [ -n "$got_desc" ] && desc="$got_desc"

            {
                echo "---"
                echo "name: planforge-$base"
                echo "description: $desc"
                echo "metadata:"
                echo "  author: plan-forge"
                echo "  source: .github/agents/$(basename "$agent_file")"
                echo "user-invocable: true"
                echo "argument-hint: \"Specify files or areas to review\""
                echo "---"
                echo ""
                cat "$agent_file"
            } > "$skill_file"
            green "  CREATE .claude/skills/planforge-$base/SKILL.md"
        done
    fi
