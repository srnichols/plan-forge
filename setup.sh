            echo "- After Step 0 (Specify) → \`/planforge-step1-preflight-check\`"
            echo "- After Step 2 (Harden) → \`/planforge-step3-execute-slice\`"
            echo "- After Step 4 (Sweep) → Start a NEW session for Step 5 (Review)"
            echo "- After Step 5 (Review) → \`/planforge-step6-ship\`"
            echo ""
            echo "## Project Instructions"
            echo ""
            cat "$copilot_instr"
            echo ""

            # Embed all instruction files by domain
            local instr_dir="$target/.github/instructions"
            if [ -d "$instr_dir" ]; then
                echo "---"
                echo ""
                echo "## Guardrail Files"
                echo ""
                echo "The following guardrails are auto-loaded in VS Code Copilot by file type. In Claude, they are embedded here for reference."
                echo ""
