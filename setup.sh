                echo "description: $desc"
                echo "metadata:"
                echo "  author: plan-forge"
                echo "  source: .github/prompts/$(basename "$prompt_file")"
                echo "user-invocable: true"
                echo "argument-hint: \"Provide context or parameters for this prompt\""
                echo "---"
                echo ""
