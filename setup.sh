#!/usr/bin/env bash
#
# Plan Forge — Project Setup Wizard (Bash)
#
# Usage:
#   ./setup.sh --preset dotnet --path ~/projects/MyApp --name MyApp
#   ./setup.sh                  # Interactive mode
#   ./setup.sh --force          # Overwrite existing files
#
# Presets: dotnet, typescript, python, java, go, swift, rust, php, azure-iac, custom

set -euo pipefail

TEMPLATE_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# ─── Defaults ──────────────────────────────────────────────────────────
PRESET=""
PROJECT_PATH=""
PROJECT_NAME=""
FORCE=false
AUTO_DETECT=false
AGENTS="copilot"
GENERIC_DIR=".ai"

# ─── Color helpers ─────────────────────────────────────────────────────
cyan()   { printf '\033[0;36m%s\033[0m\n' "$*"; }
green()  { printf '\033[0;32m%s\033[0m\n' "$*"; }
yellow() { printf '\033[0;33m%s\033[0m\n' "$*"; }
red()    { printf '\033[0;31m%s\033[0m\n' "$*"; }

# ─── Parse arguments ──────────────────────────────────────────────────
while [[ $# -gt 0 ]]; do
    case "$1" in
        --preset|-p)  PRESET="$2"; shift 2 ;;
        --path)       PROJECT_PATH="$2"; shift 2 ;;
        --name|-n)    PROJECT_NAME="$2"; shift 2 ;;
        --agent)      AGENTS="$2"; shift 2 ;;
        --generic-dir) GENERIC_DIR="$2"; shift 2 ;;
        --force|-f)   FORCE=true; shift ;;
        --auto-detect|-a) AUTO_DETECT=true; shift ;;
        --help|-h)
            echo "Usage: ./setup.sh [--preset dotnet|typescript|python|java|go|swift|rust|php|azure-iac|custom] [--path DIR] [--name NAME] [--agent copilot|claude|cursor|codex|gemini|generic|all] [--generic-dir DIR] [--force] [--auto-detect]"
            exit 0 ;;
        *) red "Unknown option: $1"; exit 1 ;;
    esac
done

# ─── Helpers ───────────────────────────────────────────────────────────
prompt_value() {
    local message="$1"
    local default="${2:-}"
    local value
    if [[ -n "$default" ]]; then
        read -rp "$message [$default]: " value
        echo "${value:-$default}"
    else
        while true; do
            read -rp "$message: " value
            [[ -n "$value" ]] && break
        done
        echo "$value"
    fi
}

copy_with_create() {
    local src="$1"
    local dst="$2"
    local dst_dir
    dst_dir="$(dirname "$dst")"

    mkdir -p "$dst_dir"

    if [[ -f "$dst" ]] && [[ "$FORCE" != true ]]; then
        yellow "  SKIP  $dst (exists)"
        return 1
    fi

    cp "$src" "$dst"
    green "  COPY  $dst"
    return 0
}

replace_placeholders() {
    local file="$1"
    local name="$2"
    local stack="$3"
    local today
    today="$(date +%Y-%m-%d)"

    [[ ! -f "$file" ]] && return

    if [[ "$(uname)" == "Darwin" ]]; then
        sed -i '' "s|<YOUR PROJECT NAME>|${name}|g" "$file"
        sed -i '' "s|<YOUR TECH STACK>|${stack}|g" "$file"
        sed -i '' "s|<YOUR BUILD COMMAND>|${BUILD_CMD}|g" "$file"
        sed -i '' "s|<YOUR TEST COMMAND>|${TEST_CMD}|g" "$file"
        sed -i '' "s|<YOUR LINT COMMAND>|${LINT_CMD}|g" "$file"
        sed -i '' "s|<YOUR DEV COMMAND>||g" "$file"
        sed -i '' "s|<DATE>|${today}|g" "$file"
    else
        sed -i "s|<YOUR PROJECT NAME>|${name}|g" "$file"
        sed -i "s|<YOUR TECH STACK>|${stack}|g" "$file"
        sed -i "s|<YOUR BUILD COMMAND>|${BUILD_CMD}|g" "$file"
        sed -i "s|<YOUR TEST COMMAND>|${TEST_CMD}|g" "$file"
        sed -i "s|<YOUR LINT COMMAND>|${LINT_CMD}|g" "$file"
        sed -i "s|<YOUR DEV COMMAND>||g" "$file"
        sed -i "s|<DATE>|${today}|g" "$file"
    fi
}

