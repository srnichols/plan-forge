    PRESETS[$i]="$(echo "${PRESETS[$i]}" | tr -d ' ')"
done

VALID_PRESETS=(dotnet typescript python java go azure-iac custom)
for p in "${PRESETS[@]}"; do
    valid=false
    for v in "${VALID_PRESETS[@]}"; do [[ "$p" == "$v" ]] && valid=true && break; done
    if [[ "$valid" != true ]]; then
        red "  ERROR  Unknown preset '$p'. Valid values: ${VALID_PRESETS[*]}"
        exit 1
    fi
done

