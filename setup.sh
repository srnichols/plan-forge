IS_CUSTOM_ONLY=false
if [[ "${#PRESETS[@]}" -eq 1 ]] && [[ "${PRESETS[0]}" == "custom" ]]; then
    IS_CUSTOM_ONLY=true
fi

# Primary preset drives build/test/lint defaults
PRIMARY_PRESET="custom"
for p in "${PRESETS[@]}"; do
    if [[ "$p" != "custom" ]]; then PRIMARY_PRESET="$p"; break; fi
done

